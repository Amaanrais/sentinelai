# SECTION 4: Assembly, deduplication, balancing, splitting
# ---------------------------------------------------------------------------

def expand_seeds(seeds: dict) -> list[dict]:
    """Flatten curated seed dict into row list."""
    rows = []
    for category, data in seeds.items():
        meta = {k: v for k, v in data.items() if k != "prompts"}
        for prompt in data["prompts"]:
            rows.append({"prompt": prompt.strip(), **meta})
    return rows


def deduplicate(rows: list[dict]) -> list[dict]:
    seen: set[str] = set()
    out = []
    for r in rows:
        key = hashlib.md5(r["prompt"].lower().strip().encode()).hexdigest()
        if key not in seen:
            seen.add(key)
            out.append(r)
    return out


def balance_dataset(df: pd.DataFrame, target_total: int = 3000) -> pd.DataFrame:
    """Balance to ~50/50 label split, capping each class at target_total // 2."""
    half = target_total // 2
    malicious = df[df["label"] == 1]
    benign = df[df["label"] == 0]
    mal_sample = malicious.sample(n=min(half, len(malicious)), random_state=RANDOM_SEED)
    ben_sample = benign.sample(n=min(half, len(benign)), random_state=RANDOM_SEED)
    return (pd.concat([mal_sample, ben_sample])
            .sample(frac=1, random_state=RANDOM_SEED)
            .reset_index(drop=True))


def add_uid(df: pd.DataFrame) -> pd.DataFrame:
    df.insert(0, "uid", [
        hashlib.md5(p.encode()).hexdigest()[:12]
        for p in df["prompt"]
    ])
    return df


def split_dataset(df: pd.DataFrame) -> tuple[pd.DataFrame, pd.DataFrame, pd.DataFrame]:
    """Stratified 70/15/15 split on label x attack_type."""
    buckets: dict[tuple, list] = defaultdict(list)
    for idx in df.index:
        key = (int(df.at[idx, "label"]), df.at[idx, "attack_type"])
        buckets[key].append(idx)

    train_idx, val_idx, test_idx = [], [], []
    rng = random.Random(RANDOM_SEED)
    for indices in buckets.values():
        indices = list(indices)
        rng.shuffle(indices)
        n = len(indices)
        n_train = int(n * 0.70)
        n_val = int(n * 0.15)
        train_idx += indices[:n_train]
        val_idx += indices[n_train:n_train + n_val]
        test_idx += indices[n_train + n_val:]

    train_df = df.loc[train_idx].sample(frac=1, random_state=RANDOM_SEED).reset_index(drop=True)
    val_df   = df.loc[val_idx].sample(frac=1, random_state=RANDOM_SEED).reset_index(drop=True)
    test_df  = df.loc[test_idx].sample(frac=1, random_state=RANDOM_SEED).reset_index(drop=True)
    return train_df, val_df, test_df


