def pull_huggingface_sources() -> list[dict]:
    """Pull from open HuggingFace datasets. Falls back gracefully if unavailable."""
    rows = []

    try:
        from datasets import load_dataset

        # -- Source [1]: jackhhao/jailbreak-classification --
        try:
            print("[*] Pulling jackhhao/jailbreak-classification ...")
            ds = load_dataset("jackhhao/jailbreak-classification", split="test")
            before = len(rows)
            for item in ds:
                text = item.get("prompt", "").strip()
                lbl = 1 if str(item.get("type", "")).lower() == "jailbreak" else 0
                if not text:
                    continue
                rows.append({
                    "prompt": text,
                    "label": lbl,
                    "attack_type": "jailbreak_explicit" if lbl == 1 else "none",
                    "injection_vector": "direct" if lbl == 1 else "none",
                    "surface_benignity": 0 if lbl == 1 else 3,
                    "obfuscation_method": "none",
                    "topic": "general",
                    "source": "jackhhao/jailbreak-classification [1]",
                })
            print(f"    -> {len(rows) - before} rows")
        except Exception as e:
            print(f"    ! jackhhao failed: {e}")

        # -- Source [2]: Harelix/Prompt-Injection-Mixed-Techniques-2024 --
        try:
            print("[*] Pulling Harelix/Prompt-Injection-Mixed-Techniques-2024 ...")
            before = len(rows)
            ds = load_dataset("Harelix/Prompt-Injection-Mixed-Techniques-2024", split="train")
            for item in ds:
                text = item.get("prompt", item.get("text", "")).strip()
                lbl = int(item.get("label", 1))
                atype = str(item.get("attack_type", "injection")).lower()
                if not text:
                    continue
                rows.append({
                    "prompt": text,
                    "label": lbl,
                    "attack_type": atype if lbl == 1 else "none",
                    "injection_vector": "direct",
                    "surface_benignity": 1,
                    "obfuscation_method": "none",
                    "topic": "mixed_techniques",
                    "source": "Harelix/Prompt-Injection-Mixed-Techniques-2024 [2]",
                })
            print(f"    -> {len(rows) - before} rows")
        except Exception as e:
            print(f"    ! Harelix failed: {e}")

        # -- Source [3]: allenai/wildjailbreak (adversarial subset only) --
        try:
            print("[*] Pulling allenai/wildjailbreak (adversarial) ...")
            before = len(rows)
            ds = load_dataset("allenai/wildjailbreak", "adversarial", split="train[:1000]")
            for item in ds:
                text = item.get("adversarial", item.get("prompt", "")).strip()
                if not text:
                    continue
                rows.append({
                    "prompt": text,
                    "label": 1,
                    "attack_type": "adversarial_jailbreak",
                    "injection_vector": "direct",
                    "surface_benignity": 2,
                    "obfuscation_method": "fictional_wrapper",
                    "topic": "adversarial",
                    "source": "allenai/wildjailbreak [3]",
                })
            print(f"    -> {len(rows) - before} rows")
        except Exception as e:
            print(f"    ! wildjailbreak failed: {e}")

        # -- Source [4]: rubend18/ChatGPT-Jailbreak-Prompts --
        try:
            print("[*] Pulling rubend18/ChatGPT-Jailbreak-Prompts ...")
            before = len(rows)
            ds = load_dataset("rubend18/ChatGPT-Jailbreak-Prompts", split="train")
            for item in ds:
                text = item.get("text", item.get("prompt", "")).strip()
                if not text:
                    continue
                rows.append({
                    "prompt": text,
                    "label": 1,
                    "attack_type": "persona_jailbreak",
                    "injection_vector": "direct",
                    "surface_benignity": 1,
                    "obfuscation_method": "role_play",
                    "topic": "jailbreak_dan",
                    "source": "rubend18/ChatGPT-Jailbreak-Prompts [4]",
                })
            print(f"    -> {len(rows) - before} rows")
        except Exception as e:
            print(f"    ! rubend18 failed: {e}")

        # -- Source [5]: fka/awesome-chatgpt-prompts (benign) --
        try:
            print("[*] Pulling fka/awesome-chatgpt-prompts (benign) ...")
            before = len(rows)
            ds = load_dataset("fka/awesome-chatgpt-prompts", split="train")
            for item in ds:
                text = item.get("prompt", "").strip()
                if not text:
                    continue
                rows.append({
                    "prompt": text,
                    "label": 0,
                    "attack_type": "none",
                    "injection_vector": "none",
                    "surface_benignity": 3,
                    "obfuscation_method": "none",
                    "topic": "benign_system_prompt",
                    "source": "fka/awesome-chatgpt-prompts [5]",
                })
            print(f"    -> {len(rows) - before} rows")
        except Exception as e:
            print(f"    ! fka/awesome-chatgpt-prompts failed: {e}")

    except ImportError:
        print("[!] 'datasets' package not installed. Run: pip install datasets")
        print("    Skipping HuggingFace sources -- using synthetic only.\n")

    return rows


